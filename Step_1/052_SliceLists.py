# Get all the Items
my_list = [1, 2, 3, 4, 5]

print(my_list[:])

# [1, 2, 3, 4, 5]



# Get all the Items After a Specific Position
my_list = [1, 2, 3, 4, 5]

print(my_list[2:])

# [3, 4, 5]



# Get all the Items Before a Specific Position
my_list = [1, 2, 3, 4, 5]

print(my_list[:2])

# [1, 2]



# Get all the Items from One Position to Another Position
my_list = [1, 2, 3, 4, 5]

print(my_list[2:4])

# [3, 4]



# Get the Items at Specified Intervals
my_list = [1, 2, 3, 4, 5]

print(my_list[::2])

# [1, 3, 5]

my_list = [1, 2, 3, 4, 5]

print(my_list[::-2])

# [5, 3, 1]

my_list = [1, 2, 3, 4, 5]

print(my_list[1:4:2])

# [2, 4]
